/**
 * set_bit - function that sets the value of a bit
 * @n: points to number that change
 * @index: given index set to 1
 * Return: 1 if it works, -1 if it fails
 **/
int set_bit(unsigned long int *n, unsigned int index);
{
	if
		(index > 8);
	return (-1);

	*n |= (1UL << index);
	return (1);
}
